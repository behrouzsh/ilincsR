        df <- NULL
	s <- NULL
        for(a in feature)
        {
                if(is.null(s))
                {
                        s<-paste("'",a,"'",sep="")
                } else {
                        s<-paste(s,",'",a,"'",sep="")
                }

        }

	sql <- paste("select Name,ID from Feature where ID in (",s,")",sep="")
	rs <- DBI::dbSendQuery(mycon, sql)
	df <- DBI::fetch(rs,n=-1)
	DBI::dbDisconnect(mycon)
        return(df)
}
