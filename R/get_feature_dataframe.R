get_feature_dataframe <- function(db,feature, debuging=FALSE){
	servSet <- getServerSettings(debuging)
	mycon <- DBI::dbConnect(RMySQL::MySQL(), user = "public", dbname = db, port = servSet$port, host = servSet$host, password = "public")
#        if (!test) {
